    private void CarregarCombo() {
        String sql;
        try
        {
            cbxRA1.removeAllItems();
            cbxRA2.removeAllItems();
            cbxAluno.removeAllItems();
            cbxRA1.addItem("Selecione...");
            cbxRA2.addItem("Selecione...");
            cbxAluno.addItem("Selecione...");
                       
            sql = "select RA from Aluno order by RA ASC ";
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next())
            {
                cbxRA1.addItem(rs.getString("RA"));
                cbxRA2.addItem(rs.getString("RA"));
            }
            
            sql = "select Nome from Matricula order by Nome ASC";
            stm = con.createStatement();
            rs = stm.executeQuery(sql);
            while(rs.next())
            {
                cbxAluno.addItem(rs.getString("Nome"));
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Erro ao incluir os combos. "+e,"ERRO: ",JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
